# Funktionen definieren und aufrufen:

# Die Definition einer Funktion startet immer mit "def" und danach einen Bezeichner.
# Diese von mir definierte Funktion kann ich jetzt permanent im Programm aufrufen.
def greeting():
    print("Hallo Welt.")  # Funktionskörper: Hier kommen alle Anweisungen rein, die dann von der Funktion ausgeführt
    print("7 Kontinente")  # werden sollen, wenn die Funktion von mir später aufgerufen wird.
    print("Meine erste Funktion.")


# Aufrufen/Ausführen der Funktion:
greeting()
print("Ich bin das Hauptprogramm.")
greeting()
print("Ich bin immernoch das Hauptprogramm.")
greeting()
